// eslint-disable-next-line no-unused-vars
module.exports = function (options = {}) {
  return async context => {
    context.data.updatedAt = new Date();
    return context;
  };
};

function checkMail(mail){
	var emailRegex = /^(([^<>()[\]\.,;:\s@\"]+(\.[^<>()[\]\.,;:\s@\"]+)*)|(\".+\"))@(([^<>()[\]\.,;:\s@\"]+\.)+[^<>()[\]\.,;:\s@\"]{2,})$/i;
	return emailRegex.test(mail);
}
